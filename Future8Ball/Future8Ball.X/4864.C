#define SSD1306_ADDRESS     0x3c		//0x78   // slave device address		

uint8_t SSD1306_Read(uint8_t reg, uint8_t *pData)
{
    I2C2_MESSAGE_STATUS status = I2C2_MESSAGE_PENDING;
    static I2C2_TRANSACTION_REQUEST_BLOCK trb[2];
    
    I2C2_MasterWriteTRBBuild(&trb[0], &reg, 1, SSD1306_ADDRESS);
    I2C2_MasterReadTRBBuild(&trb[1], pData, 1, SSD1306_ADDRESS);                
    I2C2_MasterTRBInsert(2, &trb[0], &status);
    
    while(status == I2C2_MESSAGE_PENDING);      // blocking

    return (status == I2C2_MESSAGE_COMPLETE); 
} 

uint8_t SSD1306_Write(uint8_t reg, uint8_t Data)
{
    I2C2_MESSAGE_STATUS status = I2C2_MESSAGE_PENDING;
    uint8_t         write_buffer[2];
    write_buffer[0] = reg;
    write_buffer[1] = Data;
    I2C2_MasterWrite(&write_buffer, 2, SSD1306_ADDRESS, &status);  // pointer la valoarea wr_data
    while(status == I2C2_MESSAGE_PENDING);
    return (status == I2C2_MESSAGE_COMPLETE);
} 

